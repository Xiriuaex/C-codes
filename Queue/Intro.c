
/*
Queue is a list or collectionn with the restriction
that insertion can be performed at one end(rear) and 
deletion can be performed at other end(front).

Operation:
enQueue(x): insert
deQueue() : delete
front() or Peek() : return the top element
isEmpty()
isFull()
 
All takes constant time - O(1)

Applications:
    1.Printer queue
    2.Process Scheduling
    3.Simulating wait
*/